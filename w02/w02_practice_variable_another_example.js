let note = "A";
switch (note) {
    case "A":
        console.log("Excellent");
        break;
    case "B":
        console.log("Très bien");
        break;
    case "C":
        console.log("Bien");
        break;
    case "D":
        console.log("Satisfaisant");
        break;
    case "E":
        console.log("Insuffisant");
        break;
    default:
        console.log("Note non reconnue");
}