let jour = 3;
switch (jour) {
    case 1:
        console.log("Lundi");
        break;
    case 2:        
        console.log("Mardi");
        break;
    case 3:
        console.log("Mercredi");
        break;
    case 4:
        console.log("Jeudi");
        break;
    case 5:
        console.log("Vendredi");
        break;
    default:
        console.log("C'est le week-end");
}